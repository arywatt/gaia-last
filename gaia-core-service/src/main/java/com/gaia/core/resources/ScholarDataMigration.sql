/*!50003 CREATE*/ /*!50020 DEFINER=`root`@`localhost`*/ /*!50003 PROCEDURE `fill_prerequisites`()
BEGIN
	DECLARE done INT DEFAULT 1;
	DECLARE max_tp INT;
	SELECT MAX(tp) FROM baseucaouut.coursrequis INTO max_tp;

	DELETE FROM scholar.prerequisite_units;
	DELETE FROM scholar.lesson_units_prerequisite_units;

	DROP TABLE IF EXISTS scholar.prerequisite_units_tmp;
	CREATE TABLE scholar.prerequisite_units_tmp (
		id bigint(20) PRIMARY KEY AUTO_INCREMENT,
		lesson_unit_id bigint(20)
	);

	DROP TABLE IF EXISTS scholar.lesson_units_prerequisite_units_tmp;
	CREATE TABLE scholar.lesson_units_prerequisite_units_tmp (	
		lesson_unit_id bigint(20),
		prerequisite_unit_id bigint(20),
		PRIMARY KEY(lesson_unit_id, prerequisite_unit_id)
	);

	fill_loop: LOOP	

		DELETE FROM scholar.prerequisite_units_tmp;
		DELETE FROM scholar.lesson_units_prerequisite_units_tmp;

		INSERT INTO scholar.prerequisite_units_tmp(lesson_unit_id)
		SELECT DISTINCT idcours
		FROM baseucaouut.coursrequis
		WHERE tp = done;

		INSERT INTO scholar.lesson_units_prerequisite_units_tmp(prerequisite_unit_id, lesson_unit_id)
		SELECT pr.id, idprerequis
		FROM baseucaouut.coursrequis cr, scholar.prerequisite_units_tmp pr
		WHERE cr.idcours = pr.lesson_unit_id AND tp = done;
		
		INSERT INTO scholar.prerequisite_units(id, version, lesson_unit_id)
		SELECT id, 0, lesson_unit_id
		FROM scholar.prerequisite_units_tmp;

		INSERT INTO scholar.lesson_units_prerequisite_units(prerequisite_unit_id, lesson_unit_id, version)
		SELECT prerequisite_unit_id, lesson_unit_id, 0
		FROM scholar.lesson_units_prerequisite_units_tmp;

		IF done = max_tp THEN
			LEAVE fill_loop;
		END IF;

		SET done = done + 1;	

	END LOOP;

	DROP TABLE IF EXISTS scholar.prerequisite_units_tmp;
	DROP TABLE IF EXISTS scholar.lesson_units_prerequisite_units_tmp;

END */;;
